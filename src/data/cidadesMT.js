// Função utilitária para classificar qualidade do ar
export const classificarQualidadeAr = (indice) => {
  if (indice <= 50) return { classe: "text-green-500", texto: "Boa" };
  if (indice <= 100) return { classe: "text-yellow-500", texto: "Moderada" };
  if (indice <= 150) return { classe: "text-orange-500", texto: "Ruim" };
  if (indice <= 200) return { classe: "text-red-500", texto: "Muito Ruim" };
  return { classe: "text-purple-500", texto: "Péssima" };
};
